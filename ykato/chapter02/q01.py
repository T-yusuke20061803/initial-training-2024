print("リストのaの長さを求める。")
a = [4, 8, 3, 4, 1]
res = len(a)
print(res)

print("リストaに含まれる値の最大値を求める。")
res = max(a)
print(res)

print("リストaに含まれる値の最小値を求める")
res = min(a)
print(res)

print("リストaに含まれる値の合計値を求める。")
res = sum(a)
print(res)

print("リストaをソートして、[1, 3, 4, 4, 8]というリストを返す。")
res = sorted(a)
print(res)
